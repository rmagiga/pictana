/// EXIF Orientation 値から回転角度を抽出する純粋関数。
///
/// [orientation] は EXIF Orientation タグの値（1〜8）。
/// ミラーリング成分は無視し、回転成分のみを返す (Req 6.5)。
///
/// 戻り値: 0, 90, 180, 270 のいずれか。
/// 範囲外の値（≤0 または ≥9）に対しては 0 を返す。
int exifOrientationToRotation(int orientation) => switch (orientation) {
  1 => 0,
  2 => 0, // 水平ミラー → 回転なし
  3 => 180,
  4 => 180, // 垂直ミラー → 180°
  5 => 270, // 水平ミラー + 270° → 270°
  6 => 90,
  7 => 90, // 水平ミラー + 90° → 90°
  8 => 270,
  _ => 0, // 不明値 → 回転なし
};
